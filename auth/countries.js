  // Safe to call more than once on the same <select> (e.g. re-filling a
  // form after a save) — always clears any countries it previously added
  // first, leaving only the placeholder option in place.
  function populateCountrySelect(select) {
    if (!select) {
      return;
    }
    Array.prototype.slice.call(select.querySelectorAll("option[data-vybridge-country]")).forEach(function (opt) {
      opt.remove();
    });
    COUNTRIES.forEach(function (name) {
      var option = document.createElement("option");
      option.value = name;
      option.textContent = name;
      option.setAttribute("data-vybridge-country", "1");
      select.appendChild(option);
    });
  }

